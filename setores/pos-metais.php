            <button class="faq-question" id="faq-pm-q1" aria-expanded="false" aria-controls="faq-pm-a1">
              O que é uma explosão de poeira e como prevenir?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-pm-a1" role="region" aria-labelledby="faq-pm-q1">
              <p>Uma explosão de poeira ocorre quando partículas finas combustíveis (como pós metálicos) em suspensão no ar encontram uma fonte de ignição em ambiente confinado. O pentágono da explosão exige: combustível (pó), oxigênio, fonte de ignição, dispersão e confinamento. A prevenção envolve controle de acúmulo de pó, ventilação adequada, aterramento de equipamentos, detecção de faíscas e, quando necessário, sistemas de supressão ou alívio de explosão.</p>
            </div>
          </div>

          <div class="faq-item fade-in">
            <button class="faq-question" id="faq-pm-q2" aria-expanded="false" aria-controls="faq-pm-a2">
              Qual a diferença entre filtro de manga e ciclone?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-pm-a2" role="region" aria-labelledby="faq-pm-q2">
              <p>O ciclone separa partículas por força centrífuga, sendo eficiente para partículas maiores (acima de 10 µm) e servindo como pré-separador. O filtro de manga utiliza um meio filtrante (tecido) para capturar partículas muito menores, atingindo eficiências superiores a 99% para PM2.5. Em muitos projetos, usamos ambos em série: ciclone como primeiro estágio e filtro de manga como acabamento.</p>
            </div>
          </div>

          <div class="faq-item fade-in">
            <button class="faq-question" id="faq-pm-q3" aria-expanded="false" aria-controls="faq-pm-a3">
              Como dimensionar a velocidade de transporte nos dutos?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-pm-a3" role="region" aria-labelledby="faq-pm-q3">
              <p>A velocidade de transporte deve ser mantida acima do mínimo para evitar deposição de material no duto. Para pós metálicos, tipicamente varia de 18 a 25 m/s, dependendo da densidade e granulometria do material. Velocidades abaixo do mínimo causam acúmulo, entupimentos e risco de incêndio; velocidades excessivas aumentam desgaste abrasivo e consumo energético.</p>
