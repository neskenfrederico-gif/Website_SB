          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-1" id="faq-q-1">
              Qual a importância do PMOC e os riscos legais?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-1" role="region" aria-labelledby="faq-q-1">
              <p>O PMOC (Plano de Manutenção, Operação e Controle) é obrigatório por lei (Lei 13.589/2018) para todos os edifícios climatizados. A falta dele pode acarretar multas de até R$ 200 mil, além de riscos à saúde dos ocupantes e processos trabalhistas por qualidade do ar inadequada (Síndrome do Edifício Doente).</p>
            </div>
          </div>
          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-2" id="faq-q-2">
              Como o projeto em BIM reduz custos de obra?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-2" role="region" aria-labelledby="faq-q-2">
              <p>O BIM (Building Information Modeling) permite a "construção virtual" antes da obra real. Através do *Clash Detection* (detecção de interferências), identificamos conflitos entre ar-condicionado, elétrica e hidráulica ainda no projeto, evitando retrabalhos caros e atrasos no canteiro de obras.</p>
            </div>
          </div>
          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-3" id="faq-q-3">
              Retrofit de sistemas antigos vale a pena?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-3" role="region" aria-labelledby="faq-q-3">
              <p>Sim. Sistemas antigos (15+ anos) costumam consumir até 40% mais energia. Um retrofit bem planejado, com substituição de chillers e automação, pode ter ROI (Retorno sobre Investimento) inferior a 3 anos apenas com a economia de energia, além de reduzir drasticamente custos de manutenção corretiva.</p>
            </div>
          </div>
          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-4" id="faq-q-4">
              Quais normas vocês seguem para Áreas Limpas?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-4" role="region" aria-labelledby="faq-q-4">
              <p>Seguimos rigorosamente a ISO 14644 para classificação de partículas, além das normas da ANVISA (RDC) específicas para fármacos e saúde. Projetamos com controle de cascata de pressão, eclusas e filtragem HEPA/ULPA para garantir a integridade do produto e segurança operacional.</p>
            </div>
          </div>
          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-5" id="faq-q-5">
              Qual a diferença entre LOD 300 e LOD 400 no BIM?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-5" role="region" aria-labelledby="faq-q-5">
              <p>LOD 300 é o nível de desenvolvimento para coordenação (dimensões aproximadas e localização). LOD 400 inclui detalhamento para fabricação: suportes, conexões, acessórios e informações para instalação direta em campo. Entregamos LOD 400 para minimizar improvisações na obra.</p>
            </div>
          </div>
          <div class="faq-item fade-in">
            <button class="faq-question" aria-expanded="false" aria-controls="faq-answer-6" id="faq-q-6">
              O que é comissionamento (Cx) de sistemas HVAC?
              <span class="faq-icon" aria-hidden="true">+</span>
            </button>
            <div class="faq-answer" id="faq-answer-6" role="region" aria-labelledby="faq-q-6">
              <p>Comissionamento é o processo sistemático de verificação e documentação de que todos os sistemas estão instalados, calibrados e funcionando conforme especificado no projeto. Inclui TAB (testes, ajustes e balanceamento), verificação de controles e treinamento da equipe de operação.</p>
            </div>
          </div>
